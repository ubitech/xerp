package com.mycompany.xerp.authentication;

/**
 *
 * @author smantzouratos
 */
public class AUserRole {
    
    String role;
    int id;

    public AUserRole(String role, int id) {
        this.role = role;
        this.id = id;
    }
    
    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
